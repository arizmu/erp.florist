        <script>
            function productionIndex() {
                return {
                    data: [],
                    getData() {
                        const url = "/produksi/get-data-produksi";
                        axios.get(url).then(res => {
                            const data = res.data.data;
                            this.data = data;
                        }).catch(erres => {

                        }).finally(() => {

                        })
                    },
                    toComplate(index) {
                        Swal.fire({
                            title: "Konfirmasi",
                            text: "Apakah bucket selesai produksi ?",
                            icon: "question",
                            showCancelButton: true,
                            confirmButtonColor: "#3085d6",
                            cancelButtonColor: "#d33",
                            confirmButtonText: "Ya, Selesai",
                            cancelButtonText: "Batal"
                        }).then((result) => {
                            if (result.isConfirmed) {
                                Swal.fire({
                                    icon: 'info',
                                    title: 'Processing...',
                                    html: 'Mohon tunggu sebentar',
                                    allowOutsideClick: false,
                                    didOpen: () => {
                                        Swal.showLoading()
                                    }
                                });

                                axios.get(`/produksi/to-complate/${index}`)
                                    .then(response => {
                                        Swal.fire({
                                            title: "Berhasil!",
                                            text: "Data produksi berhasil diupdated.",
                                            icon: "success"
                                        });
                                    })
                                    .catch(error => {
                                        Swal.fire({
                                            title: "Error!",
                                            text: "Terjadi kesalahan saat update data.",
                                            icon: "error"
                                        });
                                    }).finally(() => {
                                        this.getData()
                                    });
                            }
                        });
                    },
                    toDistribusi(index) {
                        Swal.fire({
                            title: "Konfirmasi",
                            text: "Yakin ingin distribusi produk kedaftar penjualan ?",
                            icon: "info",
                            showCancelButton: true,
                            confirmButtonColor: "#3085d6",
                            cancelButtonColor: "#d33",
                            confirmButtonText: "Ya, Proses",
                            cancelButtonText: "Batal"
                        }).then((result) => {
                            if (result.isConfirmed) {
                                Swal.fire({
                                    icon: 'info',
                                    title: 'Processing...',
                                    html: 'Mohon tunggu sebentar',
                                    allowOutsideClick: false,
                                    didOpen: () => {
                                        Swal.showLoading()
                                    }
                                });

                                axios.get(`/produksi/distribution-to-product/${index}`)
                                    .then(response => {
                                        Swal.fire({
                                            title: "Berhasil!",
                                            text: "Distrubiton product successfully..",
                                            icon: "success"
                                        });
                                    })
                                    .catch(error => {
                                        Swal.fire({
                                            title: "Error!",
                                            text: "Distribusi gagal diproses.",
                                            icon: "error"
                                        });
                                    });
                            }
                        });
                    },

                    details: [],
                    detailFunc(item) {
                        const openModal = document.getElementById("open-modal-detail");
                        axios.get(`/produksi/get-production-detail/${item.id}`)
                            .then((res) => {
                                const data = res.data.data;
                                this.details = data;
                                openModal.click();
                            }).catch((err) => {
                                console.log(err)
                            })
                    },

                    init() {
                        this.getData()
                    }
                }
            }
        </script>
